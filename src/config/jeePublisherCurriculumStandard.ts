export type ExamType = 'JEE_MAINS' | 'JEE_ADVANCED' | 'NEET';
export type SubjectType = 'Physics' | 'Chemistry' | 'Mathematics' | 'Biology';
export type PublisherType = 'NCERT' | 'Cengage' | 'Arihant' | 'Allen' | 'PW' | 'Unacademy';
export type ClassLevel = 11 | 12;

export interface CurriculumTopic {
  name: string;
  description?: string;
}

export interface CurriculumChapter {
  name: string;
  number: number;
  classLevel: ClassLevel;
  topics: CurriculumTopic[];
  description?: string;
}

export interface Provenance {
  source: string; // e.g., 'Cengage', 'NCERT', 'synthesized'
  edition?: string; // optional edition or year
  url?: string; // optional link to TOC or publisher page
  notes?: string; // any freeform notes about mapping decisions
  version?: string; // mapping/version identifier
  timestamp?: string; // ISO timestamp when this mapping was created/annotated
}

// local types that carry optional provenance
export type CurriculumTopicWithProv = CurriculumTopic;
export type CurriculumChapterWithProv = CurriculumChapter;
export type CurriculumDataWithProv = CurriculumData;

export interface CurriculumData {
  publisher: PublisherType;
  exam: ExamType;
  subject: SubjectType;
  chapters: CurriculumChapter[];
}

const topicList = (...topics: string[]): CurriculumTopic[] => topics.map((name) => ({ name }));

const chapter = (
  number: number,
  classLevel: ClassLevel,
  name: string,
  topics: string[],
  description?: string,
): CurriculumChapter => ({
  number,
  classLevel,
  name,
  topics: topicList(...topics),
  description,
});

