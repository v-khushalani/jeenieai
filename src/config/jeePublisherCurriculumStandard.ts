// Mapping of curricula by exam and subject
const getCurriculumChapters = (exam: ExamType, subject: SubjectType): CurriculumChapter[] => {
  if (exam === 'NEET') {
    switch (subject) {
      case 'Physics':
        return [...neetPhysicsClass11, ...neetPhysicsClass12];
      case 'Chemistry':
        return [...neetChemistryClass11, ...neetChemistryClass12];
      case 'Biology':
        return [...biologyClass11, ...biologyClass12];
      case 'Mathematics':
        return []; // Mathematics not in NEET
    }
  } else {
    // JEE_MAINS and JEE_ADVANCED use the same curriculum
    switch (subject) {
      case 'Physics':
        return [...physicsClass11, ...physicsClass12];
      case 'Chemistry':
        return [...chemistryClass11, ...chemistryClass12];
      case 'Mathematics':
        return [...mathematicsClass11, ...mathematicsClass12];
      case 'Biology':
        return []; // Biology not in JEE
    }
  }
};

const buildCurricula = (): CurriculumData[] => {
  const publishers: PublisherType[] = ['NCERT', 'Cengage', 'Arihant', 'Allen', 'PW', 'Unacademy'];
  const curricula: CurriculumData[] = [];

  publishers.forEach((publisher) => {
    // JEE MAINS - Physics, Chemistry, Mathematics
    ['Physics', 'Chemistry', 'Mathematics'].forEach((subject) => {
      curricula.push({
        publisher,
        exam: 'JEE_MAINS',
        subject: subject as SubjectType,
        chapters: getCurriculumChapters('JEE_MAINS', subject as SubjectType),
      });
    });

    // JEE ADVANCED - Physics, Chemistry, Mathematics
    ['Physics', 'Chemistry', 'Mathematics'].forEach((subject) => {
      curricula.push({
        publisher,
        exam: 'JEE_ADVANCED',
        subject: subject as SubjectType,
        chapters: getCurriculumChapters('JEE_ADVANCED', subject as SubjectType),
      });
    });

    // NEET - Physics, Chemistry, Biology
    ['Physics', 'Chemistry', 'Biology'].forEach((subject) => {
      curricula.push({
        publisher,
        exam: 'NEET',
        subject: subject as SubjectType,
        chapters: getCurriculumChapters('NEET', subject as SubjectType),
      });
    });
  });

  return curricula;
};

export const STANDARD_CURRICULA: CurriculumData[] = buildCurricula();

const normalizeExam = (exam: string): ExamType | null => {
  const normalized = exam.trim().toUpperCase();
  if (normalized.includes('NEET')) return 'NEET';
  if (normalized.includes('ADVANCED')) return 'JEE_ADVANCED';
  if (normalized.includes('JEE')) return 'JEE_MAINS';
  return null;
};

const normalizeSubject = (subject: string): SubjectType | null => {
  const normalized = subject.trim().toLowerCase();
  if (normalized === 'physics') return 'Physics';
  if (normalized === 'chemistry') return 'Chemistry';
  if (normalized === 'mathematics' || normalized === 'maths' || normalized === 'math') return 'Mathematics';
  if (normalized === 'biology' || normalized === 'bio') return 'Biology';
  return null;
};

const getCurriculumBase = (publisher: string, exam: string, subject: string): CurriculumData | null => {
  const normalizedExam = normalizeExam(exam);
  const normalizedSubject = normalizeSubject(subject);
  if (!normalizedExam || !normalizedSubject) return null;

  return STANDARD_CURRICULA.find(
    (curriculum) =>
      curriculum.publisher.toLowerCase() === publisher.toLowerCase() &&
      curriculum.exam === normalizedExam &&
      curriculum.subject === normalizedSubject,
  ) || null;
};

export function getExpandedCurriculumData(
  publisher: string,
  exam: string,
  subject: string,
  classLevel?: ClassLevel,
): CurriculumData | null {
  const curriculum = getCurriculumBase(publisher, exam, subject);
  if (!curriculum) return null;

  if (!classLevel) {
    return curriculum;
  }

  return {
    ...curriculum,
    chapters: curriculum.chapters.filter((chapter) => chapter.classLevel === classLevel),
  };
}

export function getCurriculumData(
  publisher: string,
  exam: string,
  subject: string,
  classLevel?: ClassLevel,
): CurriculumData | null {
  return getExpandedCurriculumData(publisher, exam, subject, classLevel);
}

export function getPublishersForExam(exam: string): PublisherType[] {
  return normalizeExam(exam) ? ['NCERT', 'Cengage', 'Arihant', 'Allen', 'PW', 'Unacademy'] : [];
}

export function getSubjectsForPublisherExam(publisher: string, exam: string): SubjectType[] {
  const normalizedExam = normalizeExam(exam);
  if (!normalizedExam) return [];

  if (normalizedExam === 'NEET') {
    return ['Physics', 'Chemistry', 'Biology'];
  }

  return ['Physics', 'Chemistry', 'Mathematics'];
}
